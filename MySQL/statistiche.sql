    PtTOT INT NOT NULL,
    GTOT INT NOT NULL,
    VTOT INT NOT NULL,
    NTOT INT NOT NULL,
    PTOT INT NOT NULL,
    GFTOT INT NOT NULL,
    GSTOT INT NOT NULL,
    DRTOT INT NOT NULL,
    PtC INT NOT NULL,
    GC INT NOT NULL,
    VC INT NOT NULL,
    NC INT NOT NULL,
    PC INT NOT NULL,
    GFC INT NOT NULL,
    GSC INT NOT NULL,
    DRC INT NOT NULL,
    PtT INT NOT NULL,
    GT INT NOT NULL,
    VT INT NOT NULL,
    NT INT NOT NULL,
    PT INT NOT NULL,
    GFT INT NOT NULL,
    GST INT NOT NULL,
    DRT INT NOT NULL,
    PRIMARY KEY (ID_Squadra, ID_Campionato),
